function check_ranged_weapon_type()
	local ranged_weapon

	if (state.RangeSet.value == 'None' or state.UnlockWeapons.value) and (sets.midcast.RA.range or sets.midcast.RA.ranged) then
		if sets.midcast.RA.range then
			ranged_weapon = sets.midcast.RA.range
		elseif sets.midcast.RA.ranged then
			ranged_weapon = sets.midcast.RA.ranged
		end
	elseif state.RangeSet.value ~= 'None' and sets[state.RangeSet.value] and (sets[state.RangeSet.value].range or sets[state.RangeSet.value].ranged) then
		if sets[state.RangeSet.value].range then
			ranged_weapon = sets[state.RangeSet.value].range
		elseif sets[state.RangeSet.value].ranged then
			ranged_weapon = sets[state.RangeSet.value].ranged
		end
	end
	
	if not ranged_weapon and player.equipment.range then
		ranged_weapon = player.equipment.range
	else
		RangedWeaponType = 'None'
	end

	if type(ranged_weapon) == 'table' then
		ranged_weapon = ranged_weapon.name
	end
	
	set_ranged_weapon_type(ranged_weapon)
end

