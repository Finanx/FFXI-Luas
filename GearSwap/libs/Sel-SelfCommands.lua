function handle_stna(cmdParams)
	local removalTarget
	local targetBuffs

	if cmdParams[1] then
		if tonumber(cmdParams[1]) then
			removalTarget = get_party_member_by_id(tonumber(cmdParams[1]))
		else
			removalTarget = table.concat(cmdParams, ' ')
			removalTarget = get_closest_party_member_by_name(removalTarget)
		end
	end

	if not removalTarget then
		if player.target.type == "SELF" or player.target.type == 'NONE' then
			removalTarget = get_party_member_by_id(player.id)
		elseif player.target.type == 'MONSTER' then
			send_command('gs c stna <stpt>')
			return
		elseif player.target.status == 'Dead' or player.target.status == 'Engaged dead' then
			windower.chat.input('/ma "Arise" '..removalTarget.id..'')
			return
		elseif player.target.in_party then
			removalTarget = get_party_member_by_id(player.target.id)
		end
	end

	if not removalTarget then
		add_to_chat(123, 'STNA Target not found.')
		return
	elseif removalTarget.mob.status == 2 or removalTarget.mob.status == 3 then --Dead or Engaged Dead
		windower.chat.input('/ma "Arise" '..removalTarget.id..'')
		return
	end

	targetBuffs = removalTarget.buffactive

	if not targetBuffs then
		add_to_chat(123, 'STNA Target not found.')
		return
	end

	for i, status in ipairs(data.status_map) do
		local spell = status.spell
		if targetBuffs[status.buff] then
			if silent_can_cast(spell) then
				windower.chat.input('/ma "'..spell..'" '..removalTarget.name)
				return
			end
		end
	end
	
	if silent_can_cast("Erase") then
		for key in pairs(targetBuffs) do
			if type(key) == "string" and key:endswith(' down') then
				windower.chat.input('/ma "Erase" '..removalTarget.name)
				return
			end
		end
	end

	add_to_chat(123,'No statuses found that are removable by STNA.')
end

function handle_smartcure(cmdParams)
	local cureTarget
	if cmdParams[1] then
		if tonumber(cmdParams[1]) then
			cureTarget = windower.ffxi.get_mob_by_id(tonumber(cmdParams[1]))
		else
			cureTarget = table.concat(cmdParams, ' ')
			cureTarget = get_closest_mob_by_name(cureTarget)
			if not (cureTarget and cureTarget.name) then
				if player.target.type ~= 'NONE' then
					cureTarget = player.target
				else
					cureTarget = player
				end
			end
		end
	elseif player.target.type == "SELF" or player.target.type == 'NONE' then
		cureTarget = player
	elseif player.target.type == 'MONSTER' then
		send_command('gs c smartcure <stal>')
		return
	else
		cureTarget = player.target
	end
	if cureTarget.status == 'Dead' or cureTarget.status == 'Engaged dead' then
		windower.chat.input('/ma "Arise" '..cureTarget.id..'')
		return
	end

	local missingHP
	local spell_recasts = windower.ffxi.get_spell_recasts()

	if cureTarget.in_alliance then
		cureTarget.hp = find_player_in_alliance(cureTarget.name).hp
		local est_max_hp = cureTarget.hp / (cureTarget.hpp/100)
		missingHP = math.floor(est_max_hp - cureTarget.hp)
	else
		local est_current_hp = 1800 * (cureTarget.hpp/100)
		missingHP = math.floor(1800 - est_current_hp)
	end

	if missingHP < 170 then
		if spell_recasts[1] < spell_latency then
			windower.chat.input('/ma "Cure" '..cureTarget.id..'')
		elseif spell_recasts[2] < spell_latency then
			windower.chat.input('/ma "Cure II" '..cureTarget.id..'')
		else
			add_to_chat(123,'Abort: Appropriate cures are on cooldown.')
		end
	elseif missingHP < 350 then
		if spell_recasts[2] < spell_latency then
			windower.chat.input('/ma "Cure II" '..cureTarget.id..'')
		elseif spell_recasts[3] < spell_latency then
			windower.chat.input('/ma "Cure III" '..cureTarget.id..'')
		elseif spell_recasts[1] < spell_latency then
			windower.chat.input('/ma "Cure" '..cureTarget.id..'')
		else
			add_to_chat(123,'Abort: Appropriate cures are on cooldown.')
		end
	elseif missingHP < 700 then
		if spell_recasts[3] < spell_latency then
			windower.chat.input('/ma "Cure III" '..cureTarget.id..'')
		elseif spell_recasts[4] < spell_latency then
			windower.chat.input('/ma "Cure IV" '..cureTarget.id..'')
		elseif spell_recasts[2] < spell_latency then
			windower.chat.input('/ma "Cure II" '..cureTarget.id..'')
		else
			add_to_chat(123,'Abort: Appropriate cures are on cooldown.')
		end
	else
		if silent_can_cast("Cure IV") and spell_recasts[4] < spell_latency then
			windower.chat.input('/ma "Cure IV" '..cureTarget.id..'')
		elseif spell_recasts[3] < spell_latency then
			windower.chat.input('/ma "Cure III" '..cureTarget.id..'')
		elseif spell_recasts[2] < spell_latency then
			windower.chat.input('/ma "Cure II" '..cureTarget.id..'')
		else
			add_to_chat(123,'Abort: Appropriate cures are on cooldown.')
		end
	end
end

